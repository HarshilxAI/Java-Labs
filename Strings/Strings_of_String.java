package Strings;
import java.util.*;

public class Strings_of_String {

  public static void main(String[] args) {

    Scanner sc = new Scanner(System.in);
    System.out.print("Enter a string: ");


String extermee = sc.nextLine();

    System.out.println("String: " + extermee);


    int length = extermee.length();
System.out.println("Length: " + length);

    sc.close();
  } 


}
